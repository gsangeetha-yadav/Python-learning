# Basic Bitwise Operators in Python

a = 10
b = 4

# AND Operator
print("a & b =", a & b)

# OR Operator
print("a | b =", a | b)

# XOR Operator
print("a ^ b =", a ^ b)

# NOT Operator
print("~a =", ~a)

# Left Shift Operator
print("a << 1 =", a << 1)

# Right Shift Operator
print("a >> 1 =", a >> 1)
